export const Language = (language) => {

  if (language === 'fr') {

    return {
      see_project:'Voir le projet',
      the_project:'Le projet',
      context:'Contexte',
      year:'Année',
      previous:'Projet précédent',
      next:'Projet suivant',
      works:'Réalisations'
    }

  }else {

    return {
      see_project:'See the project',
      the_project:'The project',
      context:'Context',
      year:'Year',
      previous:'Previous project',
      next:'Next project',
      works:'Works'
    }

  }

}
